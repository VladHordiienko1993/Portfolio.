import {put} from "redux-saga/effects";
import * as API from "../api/index";
import {userGoogleSuccess,userGoogleError} from "../slices/userSlices";

export function* callBackGoogleSaga(action){
try {
  const {data:{data: user}} = yield API.fetchGoogleCallBack(action.payload);

  yield put(userGoogleSuccess(user));
} catch (error) {
  yield put(userGoogleError(error))
}
};

